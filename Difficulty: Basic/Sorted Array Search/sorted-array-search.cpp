class Solution{
    public:
    // Function to find element in sorted array
    // arr: input array
    // N: size of array
    // K: element to be searche
    int searchInSorted(int arr[], int N, int K) 
    { 
    
       // Your code here
       int i=0,j=N-1,m=0;
       while(i<=j){
           m= i + (j-i)/2;
           if(arr[m]==K)return 1;
           else if(arr[m]>K)j=m-1;
           else i=m+1;
       }
       return -1;
    }
};

